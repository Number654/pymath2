def convert(formula, x):
    return eval(formula.replace('x', str(x)))


def convert_speed(speed1, speed2):
    # 5 meters/minute to meters/second
    speed1 = speed1.split('/')
    speed2 = speed2.split('/')
    # Единицы измерения расстояния в строках скоростей
    length_unit1 = speed1[0].split(' ')[1].upper()
    length_unit2 = speed2[0].upper()
    # Единицы измерения времении в строках скоростей
    # Добавляем окончание -S к единицам измерения времени,
    # Чтобы можно было найти формулу переведения,
    # Ведь в строках скоростей не пишется окончание -S:
    # 5 meters/minute
    time_unit1 = speed1[1].upper() + 'S'
    time_unit2 = speed2[1].upper() + 'S'
    # Расстояние, указанное в строке скорости №1
    distance = float(speed1[0].split(' ')[0])

    # Если единицы измерения расстояния в скоростях одинаковые, то делим расстояние из скорости 1
    # На переведенные единицы измерения времени из двух скоростей
    if length_unit1 == length_unit2:
        return float(eval('%s / convert(Time.%s, 1.0)' % (distance, time_unit1 + '_TO_' + time_unit2)))
    # Если единицы измерения времени в скоростях одинаковые, то просто переводим единицы измерения расстояния из
    # Скоростей
    if time_unit1 == time_unit2:
        return float(eval('convert(Length.%s, %s)' % (length_unit1 + '_TO_' + length_unit2, distance)))
    # Если единицы измерения расстояния и времени во всех скоростях абсолютно разные, то
    # Делим переведенные единицы измерения расстояния на переведенные единицы измерения времени
    else:
        return float(eval('convert(Length.%s, %s) / convert(Time.%s, 1.0)' % (length_unit1 + '_TO_' + length_unit2,
                                                                              distance,
                                                                              time_unit1 + '_TO_' + time_unit2)))
